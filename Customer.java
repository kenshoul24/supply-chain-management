package Logistic;

public class Customer {
    private double x;
    private double y;

    // Constructor
    public Customer(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // Getter cho tọa độ
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
